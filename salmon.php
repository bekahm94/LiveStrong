                            <h2>Method</h2>
                            <h3>1.</h3><p>Lay salmon in a small glass or ceramic dish. Combine juice, salt, sugar and spices 
                                and pour over salmon, ensuring it is well covered. Cover and refrigerate for at least 4 hours 
                                or preferably overnight.</p>
				<h3>2.</h3><p>For the salad, pour the boiling water over the cracked wheat and let it soak for about 
                                    20 minutes until most of the liquid is absorbed and the wheat tender. Drain off any excess liquid.</p>
				<h3>3.</h3><p>Add vegetables and toss well. Combine oil, honey and juice and dress salad. Season with sea salt and cracked pepper.</p>
                                <h3>4.</h3><p>Serve salad topped with thinly sliced salmon, caperberries (halved or whole) and a sprinkle of chopped fennel tips.</p>
