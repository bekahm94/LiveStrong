			<div class="method">
                            <h2>Method</h2>
                            <h3>1.</h3><p>Slice chorizo into 1cm-thick rounds. Cut haloumi into 1.5cm cubes and pat dry with paper towels.</p>
				<h3>2.</h3><p>Thread a piece of chorizo, then haloumi, then a cherry tomato onto each skewer, going right through 
                                    the middle of each ingredient so that they all sit flush with each other (this will ensure they make surface
                                    contact with the BBQ/grill pan during cooking).p>
				<h3>3.</h3><p>Brush skewers with oil if cooking on the BBQ, or heat a drizzle of oil in a large fry pan or grill pan. 
                                    Cook skewers for 1-2 minutes on each side, or until the chorizo is starting to render out a bit of fat, the haloumi 
                                    is golden, and cherry tomatoes are starting to blister.</p>
                                <h3>*Creamy chipotle dipping sauce:</h3><p>mix 2 tablespoons good quality mayonnaise with 2 tablespoons natural yoghurt and 2 teaspoons chipotle sauce.</p>
                                <h3>**Coriander dipping sauce:</h3><p>blitz 1/2 cup chopped coriander, 1/2 cup mint leaves, 1 clove chopped garlic, 1 teaspoon finely grated ginger, 2-3 tablespoons 
                            extra-virgin olive oil and 2-3 tablespoons lime or lemon juice. Season to taste with salt and pepper.</p>
